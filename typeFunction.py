#type variable assigned using input() Function
a = input("Enter the value of a: ")
print(type(a))#it will give string
b = int(input("Enter the value of b: "))
print(type(b))#it will be integer
c = float(input("Enter the value of b: "))
print(type(c))#it will be float
d = bool (input("Enter the value of b: "))
print(type(d))#it will boolean